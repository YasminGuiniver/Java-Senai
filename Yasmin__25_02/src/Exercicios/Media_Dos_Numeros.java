//4. Criar um algoritmo que imprima a média aritmética entre os números 8, 9 e 7.
package Exercicios;

public class Media_Dos_Numeros {

	public static void main(String[] args) {
		double media;
		int num1 = 7;
		int num2 = 8;
		int num3 = 9;
		
		media = (num1 + num2 + num3) / 3;
		
		System.out.println("A media dos numeros 7,8,9 é: " +media);

	}

}
